#include <iostream>
using namespace std;

int main() {
    int a, b;

    // Take input from the user
    cout << "Enter the first number: ";
    cin >> a;

    cout << "Enter the second number: ";
    cin >> b;

    // Calculate the sum
    int sumResult = a + b;

    // Output the result
    cout << "The sum is: " << sumResult << endl;

    return 0;
}
